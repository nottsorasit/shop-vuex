<template>
  <header>
    <nav class="navbar">
      <span class="navbar-brand mb-0 h1">ร้านขายผลไม้(บูด)</span>
      <span class="navbar-brand mb-0 h1">
        ตะกร้าสินค้า <span class="badge badge-primary">{{ totalQty }}</span>
      </span>
    </nav>
  </header>
</template>

<script>
import { mapGetters } from "vuex";

export default {
  name: "Header",
  computed: {
    ...mapGetters(["cart"]),
    totalQty() {
      return this.cart.reduce((a, b) => a + b.qty, 0);
    },
  },
};
</script>

